package com.company;

public class Calc {
    static public int add(int a, int b) {
        return a + b;
    }

    static public int minus(int a, int b) {
        return a - b;
    }

    static public int multiply(int a, int b) {
        return a * b;
    }

    static public double divide (int a, int b)
    {
        return (float) a/b;
    }
}
